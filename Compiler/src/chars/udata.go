package chars

func CheckCp(cp int) (ok bool) { _, ok = cpMap[cp]; return }

// Sizes of data tables.
const (
	cpNumber    = 95
	cpDataSize  = 3810
	ucpDataSize = uint16(20992)
)

// Indexes of Unicode encodings
const (
	utf16le = cpNumber
	utf16be = cpNumber + 1
	utf32le = cpNumber + 2
	utf32be = cpNumber + 3
	utf8    = cpNumber + 4
)

var ucpData []uint16 = make([]uint16, ucpDataSize)

// Decompresses code pages
func init() {
	for i, j := uint16(0), uint16(0); j < ucpDataSize; {
		val := cpData[i]
		i++

		switch {
		case val < 0xD800 || val > 0xDFFF:
			ucpData[j] = val
			j++
		case val == 0xD800:
			for k := uint16(0); k < uint16(128); k++ {
				ucpData[j] = k
				j++
			}
		case val <= 0xDC00:
			length := val - 0xD800
			index := cpData[i]
			i++
			copy(ucpData[j:j+length], ucpData[index:index+length])
			j += length
		case val <= 0xDD00:
			k := cpData[i]
			i++
			limit := k + val - 0xDC00

			for ; k < limit; k++ {
				ucpData[j] = k
				j++
			}
		default:
			length := val - 0xDD00
			orig := cpData[i]
			i++
			ucpData[j] = orig
			j++

			for k := uint16(0); k < length; k += 2 {
				d := cpData[i]
				i++
				ucpData[j+k] = orig + (d >> 8)
				ucpData[j+k+1] = orig + (d & 0x00FF)
			}

			j += length
		}
	}
}

